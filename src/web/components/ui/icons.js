// Função utilitária para criar ícones com classes CSS
export const createIcon = (IconComponent, className = "w-5 h-5") => {
  return (props) => <IconComponent className={className} {...props} />;
};

// Função para criar ícones com tamanhos padrão
export const iconSizes = {
  xs: "w-3 h-3",
  sm: "w-4 h-4", 
  md: "w-5 h-5",
  lg: "w-6 h-6",
  xl: "w-8 h-8",
  "2xl": "w-10 h-10"
};

// Função para criar ícones com tamanhos específicos
export const createSizedIcon = (IconComponent, size = "md") => {
  return (props) => <IconComponent className={iconSizes[size]} {...props} />;
};